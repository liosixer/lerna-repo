    /**
     * 通过接口或文件url获取里面的json数据
     * @param {*} url 
     */
    $get(url) {
        return new Promise((resolve, reject) => {
            const xhr = new XMLHttpRequest()
            xhr.open('GET', url)
            xhr.send(null)
            xhr.onload = () => {
                resolve(JSON.parse(xhr.responseText))
            }
            xhr.onerror = () => {
                reject(JSON.parse(xhr.responseText))
            }
        })
    },
    /**
     * 获取地图的json数据，默认从公网获取，可指定本地服务的url获取
     * @param {*} opt 
     */
    getMapJSON(opt) {
        let position = opt.position; //位置 string position 默认为'china',可设置的类型有'china'、'china-cities'、'china-contour'、'world'、'province/beijing'、'province/shanghai'等，省份的position如例子中所示需要在前面加'province/'
        let positionJsonLink = opt.positionJsonLink; //地图数据源 string
        let beforeRegisterMapOnce = opt.beforeRegisterMapOnce; // 地图数据注册前执行的函数(仅执行一次) Function
        let mapURLProfix = opt.mapURLProfix; //位置请求的 URL 前缀 string 默认为 https://unpkg.com/echarts@3.6.2/map/json/

        const link = positionJsonLink || `${mapURLProfix}${position}.json`
        let mapPromise;
        if (!mapPromise[link]) {
            mapPromise[link] = this.$get(link).then(res => {
                if (beforeRegisterMapOnce) res = beforeRegisterMapOnce(res)
                return res
            })
        }
        return mapPromise[link]
    }

